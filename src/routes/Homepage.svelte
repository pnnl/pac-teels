  <h1>Welcome to the PAC/TEELs Site</h1>
  <h4>Rev. 29A, June 2018</h4>
  <div>
    <p>
      Through the Main Menu above this site allows users to access the U.S. Department of
      Energy's (DOE's) current data set of Protective Action Criteria (PAC) values in a
      variety of ways: as a searchable database, as an Excel file, and as a series of
      tables in PDF format. It also provides archived versions of the PACs for reference.
    </p>
    <p>
      PAC values in the searchable database and tables correspond to Revision 29A,
      published June 2018. This database provides information for 3,146 chemicals.
    </p>
    <p>
      Emergency exposure limits are essential components of planning for the uncontrolled
      release of hazardous chemicals. These limits, combined with estimates of exposure,
      provide the information necessary to identify and evaluate accidents for the purpose
      of taking appropriate protective actions. During an emergency response to an
      uncontrolled release, these limits may be used to evaluate the severity of the
      event, to identify potential outcomes, and to decide what protective actions should
      be taken. In anticipation of an uncontrolled release, these limits may also be used
      to estimate the consequences of an uncontrolled release and to plan emergency
      responses.
    </p>
    <p>
      DOE O 151.1C, <em>Comprehensive Emergency Management System</em>, and its supporting
      Guides require the use of Acute Exposure Guideline Levels (AEGLs) and Emergency
      Response Planning Guidelines (ERPGs) as the emergency exposure limits of choice, in
      order of preference. Because AEGLs and ERPGs exist only for a limited number of
      chemicals, DOE also commissions the development of Temporary Emergency Exposure
      Limits (TEELs) so that DOE facilities may conduct Emergency Planning Hazard
      Assessments (EPHAs) and consequence assessments during response for chemicals
      lacking AEGLs or ERPGs. TEELs are temporary limits for chemicals until AEGLs or
      ERPGs are developed. Together, AEGLs, ERPGs, and TEELs are referred to as chemical
      Protective Action Criteria - PACs.
    </p>
    <p>
      TEEL values are always subject to change, being replaced by AEGLs or ERPGs when new
      values are published, and many are updated annually when different exposure limits
      [e.g., Permissible Exposure Limits (PELs) or Threshold Limit Values (TLVs)] or new
      toxicity data are published. New chemicals for which TEEL values are derived are
      added at the same time.
    </p>
    <p>
      PACs for emergency planning of chemical release events are based on the chemical
      exposure limit values provided in:
    </p>
    <p>
      <strong>Final and interim Acute Exposure Guideline Levels (AEGLs).</strong> AEGLs
      (see
      <a href="http://www.epa.gov/oppt/aegl/pubs/sop.pdf"
        >AEGL Standing Operating Procedures</a
      >) are developed by the U.S. Environmental Protection Agency (EPA). AEGLs are
      defined for five time periods: 10 minutes, 30 minutes, 60 minutes, 4 hours, and 8
      hours. The 60-minute AEGL values have been selected for use in the PAC database.
    </p>
    <p>
      <strong>Emergency Response Planning Guidelines (ERPGs). </strong>ERPGs are produced
      by the American Industrial Hygiene Association (AIHA) Emergency Response Planning
      Committee (see
      <a
        href="https://edms.energy.gov/EM/SCAPA%20Shared%20Documents/ERP-SOPs2006.pdf"
        target="_blank">AIHA ERP Committee Procedures and Responsibilities</a
      >).
    </p>
    <p>
      <strong>Temporary Emergency Exposure Limit (TEEL) data sets. </strong>TEELs are
      developed by the DOE Office of Emergency Management (NA-41). The procedures for
      developing TEELs in Rev. 29A are described in
      <a
        href="https://www.standards.doe.gov/standards-documents/1000/1046-astd-2016/%40%40images/file"
        target="_blank">DOE-HDBK-1046-2016</a
      >, <em>Temporary Emergency Exposure Limits: Methods and Practice</em>.
    </p>
